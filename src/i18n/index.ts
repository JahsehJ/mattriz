const messages: Record<Locale, Messages> = {
	en,
	"zh-Hant": zhHant,
};

export function translate(
	locale: Locale,
	key: MessageKey,
	values: Record<string, string | number> = {},
): string {
	return messages[locale][key].replace(/\{(\w+)\}/g, (_, name: string) =>
		String(values[name] ?? `{${name}}`),
	);
}
