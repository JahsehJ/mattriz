import { readFile, writeFile } from "node:fs/promises";

const metadata = JSON.parse(
	await readFile("src/i18n/locale-metadata.json", "utf8"),
);
const paths = metadata.map(({ path }) => path).filter((path) => path !== "./");
const source = `// Generated by scripts/generate-sw-locales.mjs.\nexport const LOCALE_APP_SHELL_PATHS = ${JSON.stringify(paths)};\n`;

await writeFile("public/sw-locales.js", source);
